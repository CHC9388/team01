    <h1>可持續發展目標 (SDGs)</h1>
    <p>可持續發展目標（Sustainable Development Goals, SDGs）是聯合國於2015年提出的全球發展框架，旨在於2030年前實現17個具體的可持續發展目標，以解決全球的貧窮、不平等和氣候變遷等問題。</p>

    <h2>17個可持續發展目標</h2>
    <div class="goal">
        <table border="1"> 
        <tr>
        <th>1. 消除貧窮</th>
        <td>消除一切形式的貧窮，確保每個人都能過上有尊嚴的生活。</td>
        </tr>
        <tr>
        <th>2. 消除飢餓</th>
        <td>消除飢餓，確保所有人都能獲得安全和營養的食物。</td>
        </tr>
        <tr>
        <th>3. 健康與福祉</th>
        <td>確保所有人享有健康的生活和促進各年齡層的福祉。</td>
        </tr>
        <tr>
        <th>4. 優質教育</th>
        <td>確保人人享有公平和高質量的教育，促進終身學習機會。</td>
        </tr>
        <tr>
        <th>5. 性別平等</th>
        <td>實現性別平等，增強所有女性和女童的權能。</td>
        </tr>
        <tr>
        <th>6. 清潔飲水和衛生設施</th>
        <td>確保人人獲得水和衛生設施的可持續管理。</td>
        </tr>
        <tr>
        <th>7. 可負擔的清潔能源</th>
        <td>確保人人獲得可負擔的、可靠的、可持續的現代能源。</td>
        </tr>
        <tr>
        <th>8. 體面工作和經濟增長</th>
        <td>促進持久、包容和可持續的經濟增長，實現全體人員的充分和生產性就業。</td>
        </tr>
        <tr>
        <th>9. 產業、創新和基礎設施</th>
        <td>建立具韌性的基礎設施，促進可持續的工業化和鼓勵創新。</td>
        </tr>
        <tr>
        <th>10. 減少不平等</th>
        <td>減少國內和國家之間的不平等。</td>
        </tr>
        <tr>
        <th>11. 可持續城市和社區</th>
        <td>使城市和人類住區包容、安全、有韌性和可持續。</td>
        </tr>
        <tr>
        <th>12. 負責任消費和生產</th>        
        <td>確保可持續的消費和生產模式。</td>
        </tr>
        <tr>
        <th>13. 氣候行動</th>
        <td>採取緊急行動應對氣候變化及其影響。</td>
        </tr>
        <tr>
        <th>14. 水下生物</th>
        <td>保護和可持續利用海洋和海洋資源。</td>
        </tr>
        <tr>
        <th>15. 陸地生物</th>
        <td>保護、恢復和促進陸地生態系統的可持續利用。</td>
        </tr>
        <tr>
        <th>16. 和平、正義與強大機構</th>
        <td>促進和平和包容的社會，以可持續發展為基礎。</td>
        </tr>
        <tr>
        <th>17. 夥伴關係促進目標實現</th>
        <td>加強執行手段，振興全球夥伴關係。</td>
        </tr>
    <h2>結語</h2>
    <p>可持續發展目標為全球合作提供了一個框架，呼籲各國、企業及個人共同努力，實現更美好的未來。每個人都能參與其中，讓我們攜手共創更可持續的世界！</p>
